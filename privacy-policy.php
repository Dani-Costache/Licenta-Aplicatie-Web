  <h1>Politica de confidențialitate</h1>
  
  <p>
    Aceasta este politica noastră de confidențialitate care explică modul în care colectăm, utilizăm și protejăm informațiile personale ale utilizatorilor noștri. Vă rugăm să citiți cu atenție această politică pentru a înțelege cum tratăm datele dumneavoastră personale.
  </p>

  <h2>1. Informații colectate</h2>
  <p>
    Descrieți informațiile personale pe care le colectați, cum ar fi nume, adresă de e-mail, data nașterii, informații de contact, informații demografice etc.
  </p>

  <h2>2. Scopul utilizării informațiilor</h2>
  <p>
    Enumerați scopurile pentru care utilizați informațiile personale colectate, cum ar fi furnizarea serviciilor, personalizarea experienței utilizatorului, comunicarea și asistența clienților etc.
  </p>

  <h2>3. Securitatea datelor</h2>
  <p>
    Descrieți măsurile pe care le luați pentru a proteja datele personale ale utilizatorilor, cum ar fi utilizarea criptării, măsuri de securitate tehnice și fizice etc.
  </p>

  <h2>4. Partajarea informațiilor</h2>
  <p>
    Explicați în ce situații puteți partaja informațiile personale cu terțe părți, cum ar fi procesatorii de plăți sau partenerii de încredere.
  </p>

  <h2>5. Drepturile utilizatorilor</h2>
  <p>
    Informați utilizatorii cu privire la drepturile lor, cum ar fi dreptul de acces, dreptul de rectificare, dreptul la ștergere și cum pot exercita aceste drepturi.
  </p>

  <h2>6. Modificări ale politicii de confidențialitate</h2>
  <p>
    Indicați că vă rezervați dreptul de a modifica sau actualiza politica de confidențialitate și cum veți notifica utilizatorii cu privire la aceste modificări.
  </p>

  <h2>7. Contact</h2>
  <p>
    Furnizați informații de contact pentru întrebări sau solicitări legate de politica de confidențialitate.
  </p>
