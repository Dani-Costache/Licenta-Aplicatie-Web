  <h1>Termeni și condiții</h1>
  
  <p>
    Acești termeni și condiții stabilesc regulile și condițiile de utilizare a aplicației noastre. Prin accesarea și utilizarea aplicației, sunteți de acord să respectați și să fiți obligat de acești termeni și condiții. Vă rugăm să citiți cu atenție acești termeni și condiții înainte de a utiliza aplicația.
  </p>

  <h2>1. Utilizarea aplicației</h2>
  <p>
    Descrieți cum utilizatorii pot utiliza aplicația, ce tip de conținut este permis sau interzis, reguli de comportament etc.
  </p>

  <h2>2. Proprietatea intelectuală</h2>
  <p>
    Specificați drepturile de proprietate intelectuală ale aplicației, inclusiv drepturile de autor, mărcile înregistrate și orice alt conținut protejat.
  </p>

  <h2>3. Limitarea responsabilității</h2>
  <p>
    Clarificați că utilizarea aplicației este pe propriul risc al utilizatorului și că nu sunteți responsabil pentru orice prejudiciu sau pierdere cauzată de utilizarea aplicației.
  </p>

  <h2>4. Confidențialitatea</h2>
  <p>
    Menționați că informațiile personale ale utilizatorilor vor fi tratate conform politicii de confidențialitate și că utilizatorii ar trebui să o citească și să o înțeleagă înainte de a utiliza aplicația.
  </p>

  <h2>5. Modificări ale termenilor și condițiilor</h2>
  <p>
    Indicați că vă rezervați dreptul de a modifica sau actualiza termenii și condițiile și cum veți notifica utilizatorii cu privire la aceste modificări.
  </p>

  <h2>6. Suspendarea sau întreruperea accesului</h2>
  <p>
    Specificați că aveți dreptul de a suspenda sau întrerupe accesul utilizatorilor la aplicație în cazul încălcării termenilor și condițiilor sau pentru orice alt motiv justificat.
  </p>

  <h2>7. Contact</h2>
  <p>
    Furnizați informații de contact pentru întrebări sau solicitări legate de termenii și condițiile de utilizare.
  </p>
